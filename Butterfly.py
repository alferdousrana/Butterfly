import turtle as t

t.title("R-CODING - Butterfly")
t.width(2)
t.speed(10)

color1 = "#9497fc"
color2 = "#ff5b33"
color3 = "#b0fc94"
color4 = "#e979ff"

def go_to(pos):
    t.speed(0)
    t.up()
    t.goto(pos[0], pos[1])
    t.down()
    t.speed(10)

def rel_pos(x, y):
    t.speed(0)
    t.up()
    t.goto(t.pos() + (x, y))
    t.down()
    t.speed(10)

def draw_oval(width, height, color):
    t.speed(0)
    t.fillcolor(color)
    t.begin_fill()
    for _ in range(2):
        t.circle(height / 2, 45)
        t.circle(width / 2, 135)
    t.end_fill()
    t.speed(10)

def draw_heart(size):
    t.left(50)
    t.forward(size)
    t.circle(size * 0.5, 200)
    t.right(140)
    t.circle(size * 0.5, 200)
    t.forward(size + 5)
    t.left(50)

def right_wings(top_pos):
    go_to(top_pos)
    t.seth(-65)
    t.circle(-200, 35)
    cur_pos = t.pos()
    t.begin_fill()
    t.fillcolor(color1)
    draw_heart(100)
    t.end_fill()
    go_to((cur_pos[0], cur_pos[1] + 20))
    t.begin_fill()
    t.fillcolor(color2)
    draw_heart(80)
    t.end_fill()

    rel_pos(60, -70)
    t.seth(-45)
    draw_oval(20, 50, color3)
    rel_pos(-25, 30)
    draw_oval(10, 30, color3)
    rel_pos(-10, -15)
    draw_oval(10, 20, color4)

    go_to(top_pos)
    t.seth(-65)
    t.circle(-200, 20)
    cur_pos = t.pos()
    t.begin_fill()
    t.fillcolor(color1)
    draw_heart(100)
    t.end_fill()
    go_to(cur_pos)
    t.begin_fill()
    t.fillcolor(color2)
    draw_heart(80)
    t.end_fill()

    rel_pos(50, 30)
    t.seth(10)
    draw_oval(10, 60, color3)
    rel_pos(48, -5)
    draw_oval(20, 60, color4)
    rel_pos(-10, -40)
    draw_oval(20, 40, color3)
    t.seth(-45)
    rel_pos(0, -45)
    draw_oval(10, 80, color4)
    rel_pos(-30, 20)
    draw_oval(10, 30, color4)
    rel_pos(-30, 30)
    draw_oval(25, 30, color3)

def left_wings(top_pos):
    go_to(top_pos)
    t.seth(-110)
    t.circle(200, 22)
    cur_pos = t.pos()
    t.seth(100)
    t.begin_fill()
    t.fillcolor(color1)
    draw_heart(100)
    t.end_fill()
    go_to((cur_pos[0], cur_pos[1] + 10))
    t.begin_fill()
    t.fillcolor(color2)
    draw_heart(80)
    t.end_fill()

    rel_pos(-80, -45)
    t.seth(-135)
    draw_oval(20, 50, color3)
    rel_pos(25, 30)
    draw_oval(10, 30, color4)
    rel_pos(30, 10)
    draw_oval(10, 20, color3)

    go_to(top_pos)
    t.seth(-110)
    t.circle(200, 5)
    cur_pos = t.pos()
    t.seth(90)
    t.begin_fill()
    t.fillcolor(color1)
    draw_heart(100)
    t.end_fill()
    go_to((cur_pos[0], cur_pos[1] - 15))
    t.begin_fill()
    t.fillcolor(color2)
    draw_heart(80)
    t.end_fill()

    rel_pos(-90, 50)
    t.seth(110)
    draw_oval(10, 60, color4)
    rel_pos(40, -5)
    draw_oval(20, 60, color4)
    rel_pos(-10, -40)
    draw_oval(20, 40, color3)
    t.seth(-150)
    rel_pos(-29, -30)
    draw_oval(10, 80, color4)
    rel_pos(-30, 20)
    draw_oval(10, 30, color3)
    rel_pos(90, 15)
    draw_oval(10, 30, color3)

def draw_wings(top_pos):
    right_wings(top_pos)
    left_wings(top_pos)

def draw_butterfly():
    t.seth(70)
    t.circle(200, 45)
    top_pos = t.pos()
    t.seth(-110)
    t.circle(200, 45)
    go_to(top_pos)
    t.seth(0)
    t.begin_fill()
    t.fillcolor(color2)
    t.circle(10)
    t.end_fill()
    t.circle(10,180)
    t.width(3)
    cur_pos = t.pos()
    t.seth(90)
    t.circle(50,60)
    go_to(cur_pos)
    t.seth(90)
    t.circle(-50, 60)
    t.width(2)

    draw_wings(top_pos)
    go_to((0,0))
    t.begin_fill()
    t.fillcolor(color1)
    t.seth(70)
    t.circle(200,45)
    top_pos = t.pos()
    t.seth(-110)
    t.circle(200, 45)
    t.end_fill()

    t.begin_fill()
    t.fillcolor(color3)
    t.seth(70)
    t.circle(200, 20)
    t.seth(180)
    t.forward(30)
    t.seth(-85)
    t.circle(200, 20)
    t.end_fill()

    t.begin_fill()
    t.fillcolor(color1)
    t.seth(70)
    t.circle(200, 15)
    t.seth(180)
    t.forward(27)
    t.seth(-81)
    t.circle(200, 15)
    t.end_fill()

    go_to((0,0))
    t.begin_fill()
    t.fillcolor(color4)
    t.seth(70)
    t.circle(200,10)
    t.seth(180)
    t.forward(20)
    t.seth(-75)
    t.circle(200, 10)
    t.end_fill()

draw_butterfly()
go_to((-180, -200))
t.write("""R-CODING, Easy Coding\nPlease Subscribe!""", font=('Arial', 30, 'bold'))
t.done()
